function Update-LocaleKeys {
    param(
        [string]$Locale,
        [hashtable]$Map
    )

    $path = "web/i18n/$Locale.json"
    $obj = Get-Content -Raw $path | ConvertFrom-Json

    foreach ($k in $Map.Keys) {
        $p = $obj.PSObject.Properties[$k]
        if ($null -ne $p) {
            $p.Value = $Map[$k]
        }
    }

    foreach ($p in $obj.PSObject.Properties) {
        $v = [string]$p.Value
        $v = $v -replace '\s*\(DE\)', ''
        $v = $v -replace '\s*\(ES\)', ''
        $v = $v -replace '\s*\(RU\)', ''
        $v = $v -replace '（ZH）', ''
        $v = $v -replace '\s{2,}', ' '
        $p.Value = $v.Trim()
    }

    $json = $obj | ConvertTo-Json -Depth 100
    [System.IO.File]::WriteAllText((Resolve-Path $path), $json, (New-Object System.Text.UTF8Encoding($false)))
}

