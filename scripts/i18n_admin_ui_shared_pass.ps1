$ErrorActionPreference = 'Stop'

function Update-LocaleKeys {
    param(
        [string]$Locale,
        [hashtable]$Map
    )

    $path = "web/i18n/$Locale.json"
    $obj = Get-Content -Raw -Encoding UTF8 $path | ConvertFrom-Json

    foreach ($key in $Map.Keys) {
        $property = $obj.PSObject.Properties[$key]
        if ($null -ne $property) {
            $property.Value = $Map[$key]
        }
    }

    $json = $obj | ConvertTo-Json -Depth 100
    [System.IO.File]::WriteAllText((Resolve-Path $path), $json, (New-Object System.Text.UTF8Encoding($false)))
}

